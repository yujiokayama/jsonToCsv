export class DatePicker {
  /*
  //////////////////////////////
  properties
  //////////////////////////////
  */
  constructor() {
    this.date = new Date();
    this.year = this.date.getFullYear();
    this.month = ('0' + (this.date.getMonth() + 1)).slice(-2);
    this.day = ('0' + this.date.getDate()).slice(-2);
    this.ymd = `${this.year}${this.month}${this.day}`;
  }

  /*
  //////////////////////////////
  methods
  //////////////////////////////
  */

}
